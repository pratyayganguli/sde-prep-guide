/**
 *
 * <h2>Solving a low level design problem</h2>
 *
 * <p>
 *    To solve a low level design problem, we need to gather requirements and then start writing the code keeping in
 *    mind of the requirements following design principles and patterns which will ensure the efficiency, ease of
 *    maintenance and reusability of the code.
 * </p>
 *
 *
 * <h2>YAGNI principle</h2>
 *
 * <p>
 *     (You ain't gonna need it)
 *     YAGNI principle states that, whatever features are not mentioned in the requirements must not be added to the
 *     code although it might seem necessary so that it does not enhance the complexity of the codebase and thus making
 *     it hard to maintain.
 * </p>
 *
 * <h2>DRY principle</h2>
 *
 *<p>
 *     The DRY principle states that, do not repeat yourself, it generally means that we should reuse the existing code
 *     as much as we can to develop or integrate the existing features that align with the requirement list.
 *</p>
 *
 * <h2>UML</h2>
 *
 * <p>
 *     Unified modelling language, it is a visual modelling language, it is used to visualize, specify, construct and
 *     document the artifacts of software system.
 *
 *     Features:
 *     1. Standardization: UML is iso-certified language, ensuring consistency and compatibility across different
 *     modelling tools and methodologies.
 *
 *     2. Versatility: UML can model a large range of systems.
 *
 *     3. Visual Representation: UML uses a variety of diagrams to represent different aspects of a system, making
 *     complex systems easy to understand.
 *
 *     We will learn more about it in the diagrams section.
 * </p>
 */